#!/usr/bin/env python3
import math
"""
This script contains a function that returns the floor of a number.

Modules:
    math: Provides mathematical functions, including floor.

Functions:
    floor(n: float) -> int: Returns the floor of the given float number.
"""


def floor(n: float) -> int:
    """
    This function returns the floor of a float number.

    Args:
    n (float): The number to return its floor.

    Returns:
    int: The floor of n.
    """
    return math.floor(n)
